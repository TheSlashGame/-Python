while True:
    age_input = input("Введите ваш возраст: ")
    try:
        age = int(age_input)
        # Проверка на отрицательный возраст
        if age < 0:
            print("Ошибка: возраст не может быть отрицательным! Пожалуйста, введите корректный возраст.")
            continue
        # Проверка совершеннолетия
        if age >= 18:
            print("Вы совершеннолетний.")
        else:
            print("Вы несовершеннолетний.")
        break
    except ValueError:
        print("Ошибка: введено не число! Пожалуйста, введите целое положительное число.")
